        if len(rfc5646) == 2:
            # iso639-1
            return convert_iso639_1_to_iso639_2b(rfc5646)
        elif len(rfc5646) == 3:
            if rfc5646 in iso639_2b_to_rfc5646:
                # iso639-2b
                return rfc5646
            elif rfc5646 in iso639_2t_to_iso639_2b:
                # iso639-2t
                return iso639_2t_to_iso639_2b[rfc5646]


