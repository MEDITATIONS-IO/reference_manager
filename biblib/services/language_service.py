def find_language_authority(language):
    """ Return the language authority:
        - iso639-1
        - iso639-2b
        - iso639-2t
        - iso639-3
        - rfc5646
    """
    if language:
        language = language.lower()
        minus_index = language.find("-")
        if len(language) == 2 and language in iso639_1_to_iso639_2b:
            return "iso639-1"
        elif minus_index == 2 and language[:2] in iso639_1_to_iso639_2b:
            return "rfc5646"
        elif len(language) == 3 and language in iso639_2b_to_rfc5646:
            return "iso639-2b"
        elif len(language) == 3 and language in iso639_2t_to_rfc5646:
            return "iso639-2t"
        elif minus_index == 3 and language[:3] in iso639_2t_to_rfc5646:
            return "rfc5646"
        else:
            logging.error("Fixme ! Unknown language authority for language code: {}".format(language))
    return None


def extract_rfc5646_language(rfc5646):
    # RFC5646 langtag =
    #                   language
    #                   ["-" script]
    #                   ["-" region]
    #                   *("-" variant)
    #                   *("-" extension)
    #                   ["-" privateuse]
    # 
    # Example:
    # - "fr" (French)
    # - "de-AT" (German as used in Austria)
    # - "zh-Hant" (Chinese written using the Traditional Chinese script)
    # - "zh-Hans" (Chinese written using the Simplified Chinese script)
    # - "zh-yue-HK" (Chinese, Cantonese, as used in Hong Kong SAR)
    # - "sr-Latn-RS" (Serbian written using Latin script as used in Serbia ('RS').
    # - "es-419" (Spanish appropriate to the UN-defined Latin America and Caribbean region ('419'))
    # - "en-scotland-fonipa" (English, Scottish dialect, IPA phonetic transcription)
    # - "sl-rozaj-biske" (San Giorgio dialect of Resian dialect of Slovenian)
    # - "hy-Latn-IT-arevela" (Eastern Armenian written in Latin script, as used in Italy)
    # 
    # 
    #  language      = 2*3ALPHA            ; shortest ISO 639 code
    #                  ["-" extlang]       ; sometimes followed by
    #                                      ; extended language subtags
    #                / 4ALPHA              ; or reserved for future use
    #                / 5*8ALPHA            ; or registered language subtag
    # 
    #  extlang       = 3ALPHA              ; selected ISO 639 codes
    #                  *2("-" 3ALPHA)      ; permanently reserved
    # 
    #  script        = 4ALPHA              ; ISO 15924 code
    # 
    #  region        = 2ALPHA              ; ISO 3166-1 code
    #                / 3DIGIT              ; UN M.49 code
    # 
    #  variant       = 5*8alphanum         ; registered variants
    #                / (DIGIT 3alphanum)
    # 
    #  extension     = singleton 1*("-" (2*8alphanum))
    # 
    #                                      ; Single alphanumerics
    #                                      ; "x" reserved for private use
    #  singleton     = DIGIT               ; 0 - 9
    #                / %x41-57             ; A - W
    #                / %x59-5A             ; Y - Z
    #                / %x61-77             ; a - w
    #                / %x79-7A             ; y - z
    # 
    #  privateuse    = "x" 1*("-" (1*8alphanum))
    # 
    #  grandfathered = irregular           ; non-redundant tags registered
    #                / regular             ; during the RFC 5646 era
    # 
    #  irregular     = "en-GB-oed"         ; irregular tags do not match
    #                / "i-ami"             ; the 'langtag' production and
    #                / "i-bnn"             ; would not otherwise be
    #                / "i-default"         ; considered 'well-formed'
    #                / "i-enochian"        ; These tags are all valid,
    #                / "i-hak"             ; but most are deprecated
    #                / "i-klingon"         ; in favor of more modern
    #                / "i-lux"             ; subtags or subtag
    #                / "i-mingo"           ; combination
    #                / "i-navajo"
    #                / "i-pwn"
    #                / "i-tao"
    #                / "i-tay"
    #                / "i-tsu"
    #                / "sgn-BE-FR"
    #                / "sgn-BE-NL"
    #                / "sgn-CH-DE"
    # 
    #  regular       = "art-lojban"        ; these tags match the 'langtag'
    #                / "cel-gaulish"       ; production, but their subtags
    #                / "no-bok"            ; are not extended language
    #                / "no-nyn"            ; or variant subtags: their meaning
    #                / "zh-guoyu"          ; is defined by their registration
    #                / "zh-hakka"          ; and all of these are deprecated
    #                / "zh-min"            ; in favor of a more modern
    #                / "zh-min-nan"        ; subtag or sequence of subtags
    #                / "zh-xiang"
    # 
    #  alphanum      = (ALPHA / DIGIT)     ; letters and numbers
    # 
    # Simplified RFC5646 formats managed by this function:
    # "i-" <iana_registered_code>
    # "x-" <private_use_code>
    # <iso_639_1_code>
    # <iso_639_2_code>
    # <iso_639_1or2_code> "-" <iso_3166_code>
    # <iso_639_1or2_code> "-" <iso_15924_code>
    # <iso_639_1or2_code> "-" <iso_15924_code> "-" <iso_3166_code>

    if rfc5646:
        rfc5646 = rfc5646.lower()
        first_minus_index = rfc5646.find("-")

        if first_minus_index == 3 or len(rfc5646) == 3:
            lang = rfc5646[:3]
            if lang in iso639_2b_to_rfc5646:
                # iso639-2b ?
                result = iso639_2b_to_rfc5646[lang]
                if result:
                    return result
            if lang in iso639_2t_to_rfc5646:
                # iso639-2t ?
                result = iso639_2t_to_rfc5646[lang]
                if result:
                    return result
            else:
                logging.error("Fixme ! Unknown language format, not an iso639-2b or iso639-2t: {}".format(rfc5646))

        elif first_minus_index == 2 or len(rfc5646) == 2:
            lang = rfc5646[:2]
            if lang in iso639_1_to_iso639_2b:
                # iso639-1 ?
                return lang
            else:
                logging.error("Fixme ! Unknown language format, not an iso639-1: {}".format(rfc5646))

        elif first_minus_index == 1:
            first_char = rfc5646[:1]
            if first_char == "i":
                # iana_registered_code
                logging.error("Fixme ! iana_registered_code is not managed for rfc5646: {}".format(rfc5646))
            elif first_char == "x":
                # private_use_code
                logging.error("Fixme ! private_use_code is not managed for rfc5646: {}".format(rfc5646))
            else:
                # unknown
                logging.error("Fixme ! Unknown prefix code for rfc5646: {}".format(rfc5646))

        else:
            logging.error("Fixme ! Unknown language format for rfc5646: {}".format(rfc5646))


